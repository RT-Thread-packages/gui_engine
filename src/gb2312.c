int Gb2312ToUtf8(char *pGb2312, int len, char **ppUtf8)
{
    int ret = -1;
    int utfLen;
    char *pUtf;

    if (NULL == pGb2312 || 0 == len || NULL == ppUtf8)
    {
        rt_kprintf("error:NULL == pGb2312 || 0 == len || NULL == ppUtf8!\r\n");
        return ret;
    }

    utfLen = len + (len >> 2) + 2;
    pUtf = *ppUtf8 = (char *)rt_malloc(utfLen);
    if (NULL != pUtf)
    {
        GB2312ToUTF_8(pUtf, pGb2312, len);
        ret = 0;
    }

    return ret;
}
